#include <stdlib.h>
#include "raylib.h"

int main() {
  /*----------------*/
  /* Initialisation */
  /*----------------------------------------------------------------------------------------------*/
  const int screen_width = 800;
  const int screen_height = 600;

  InitWindow(screen_width, screen_height, "Test game");

  SetTargetFPS(240);

  int frames = 0;
  /*----------------------------------------------------------------------------------------------*/

  while (!WindowShouldClose()) {
    /*--------*/
    /* Update */
    /*--------------------------------------------------------------------------------------------*/
    frames++;
    /*--------------------------------------------------------------------------------------------*/

    /*---------*/
    /* Drawing */
    /*--------------------------------------------------------------------------------------------*/
    BeginDrawing();
    {
      ClearBackground(RAYWHITE);

      DrawText(TextFormat("Frames since start: %d", frames), 300, 300, 20, BLACK);
    }
    EndDrawing();
    /*--------------------------------------------------------------------------------------------*/
  }

  /*-------------------*/
  /* De-initialisation */
  /*----------------------------------------------------------------------------------------------*/
  CloseWindow();
  /*----------------------------------------------------------------------------------------------*/

  return EXIT_SUCCESS;
}
